package temalab12;

public class Calculator {
    int adunare(int a, int b) {
        int suma = a + b;
        return suma;
    }

    int scadere(int a, int b) {
        int diferenta = a - b;
        return diferenta;
    }

    int inmultire(int a, int b) {
        int produs = a * b;
        return produs;
    }

    float impartire(int a, int b) {
        float cat = a / b;
        return cat;
    }
}
